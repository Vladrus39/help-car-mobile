      lon: lon,
      accuracy: position.coords.accuracy
    }
    
    geolocationStatus.value = '‚úÖ –ú–µ—Å—Ç–æ–ø–æ–ª–æ–∂–µ–Ω–∏–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–æ!'
